    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        if (txtHoTen.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên cần tìm");
            txtHoTen.requestFocus();
            return;
        }
        loadDataToTable(nhr.timKiem(txtHoTen.getText()));
        JOptionPane.showMessageDialog(this, "Tìm thành công");
    }//GEN-LAST:event_btnTimActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtNaNH.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng không được để trống");
            txtNaNH.requestFocus();
            return;
        }
        if (txtHoTen.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng không được để trống");
            txtHoTen.requestFocus();
            return;
        }
        if (txtNgaySinh.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng không được để trống");
            txtNgaySinh.requestFocus();
            return;
        }
        ArrayList<NguoiHoc> lst = nhr.getAll();
        for (NguoiHoc nh : lst) {
            if (nh.getMaNH().equalsIgnoreCase(txtNaNH.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Không được trùng mã");
                txtNaNH.requestFocus();
                return;
            }
        }
        NguoiHoc nh = readForm();
        if (nhr.addNguoiHoc(nh) != null) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadDataToTable(nhr.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }

    }//GEN-LAST:event_btnAddActionPerformed

