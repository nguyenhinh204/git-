    private void loadDataToTable(ArrayList<NguoiHoc> lst) {

        model.setRowCount(0);
        for (NguoiHoc nh : lst) {
            model.addRow(new Object[]{nh.getMaNH(), nh.getHoTen(), nh.isGioiTinh(), nh.getNgaySinh()});
        }
    }

    private NguoiHoc readForm() {
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(txtNaNH.getText());
        nh.setHoTen(txtHoTen.getText());
        nh.setNgaySinh(txtNgaySinh.getText());
        if (rdoNam.isSelected()) {
            nh.setGioiTinh(false);
        }
        if (rdoNu.isSelected()) {
            nh.setGioiTinh(true);
        }
        return nh;
    }

    private void showdeTail(ArrayList<NguoiHoc> lst) {
        NguoiHoc nh = lst.get(index);
        txtNaNH.setText(nh.getMaNH());
        txtHoTen.setText(nh.getHoTen());
        txtNgaySinh.setText(nh.getNgaySinh());
        if (nh.isGioiTinh()) {
            rdoNu.setSelected(true);
        } else {
            rdoNam.setSelected(true);
        }
        tblNguoiHoc.setRowSelectionInterval(index, index);
    }
